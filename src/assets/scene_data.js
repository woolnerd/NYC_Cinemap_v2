function createGeoJSON(myData) {
  const data = myData.slice(0);

  const GeoJSON = {
    "type": "FeatureCollection",
    "features": []};

    data.forEach(film=>{
      GeoJSON.features.push(GeoJSONPoint(film["LONGITUDE"], film["LATITUDE"], film));
    })

  return GeoJSON
}


function GeoJSONPoint(long, lat, filmObj) {
  const point = 
    {
      "type": "Feature",
      "geometry": {
        "type": "Point",
        "coordinates": [
          long,
          lat
        ]
      },
      "properties": filmObj
    }
    return point;
  }


